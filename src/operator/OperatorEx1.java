package operator;

public class OperatorEx1 {

	public static void main(String args[]) {
		
		int num1 = -10;
		int num2 = 20;

		System.out.println(+num1);
		System.out.println(+num2);
		
		// -하면 부호 변함
		System.out.println(-num1);
		System.out.println(-num2);
		
		System.out.println(num1);
		System.out.println(num2);
		
		num1 -= 1;
		
		System.out.println(num1);
		
	}
	
}
